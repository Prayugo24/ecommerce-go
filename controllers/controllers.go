func SearchProduct() gin.HandlerFunc {
	return func(c *gin.Context) {
		var productList []models.Product
		var ctx, cancel = context.WithTimeout(context.Background(), 10*time.Second)
		defer cancel()
		cursor, err := ProductCollection.Find(ctx, bson.D{{}})
		if err != nil {
			c.IndentedJSON(http.StatusInternalServerError, "Somthing went wrong , please try again later")
		}
		cursor.All(ctx, &productList)
		if err != nil {
			log.Println(err)
			c.AbortWithStatus(http.StatusInternalServerError)
			return
		}
		defer cursor.Close(ctx)
		if err := cursor.Err(); err != nil {
			log.Println(err)
			c.IndentedJSON(400, gin.H{"error": err.Error()})
			return
		}
		defer cancel()
		if len(productList) == 0 {
			c.IndentedJSON(http.StatusNotFound, gin.H{"error": "No products found"})
			return
		}
		c.IndentedJSON(http.StatusOK, productList)
	}
}

func SearchProductByQuery() gin.HandlerFunc {
	return func(c *gin.Context) {
		var SearchProduct []models.Product
		queryParams := c.Query("name")

		if queryParams == "" {
			log.Println("query is empty")
			c.Header("Content-Type", "application/json")
			c.JSON(http.StatusBadRequest, gin.H{"error": "Invalid Search Index"})
			c.Abort()
			return
		}
		var ctx, cancel = context.WithTimeout(context.Background(), 10*time.Second)
		defer cancel()
		searchqueryDb, err := ProductCollection.Find(ctx, bson.M{"product_name": bson.M{"$regex": queryParams}})
		if err != nil {
			c.IndentedJSON(404, gin.H{"error": "Product Not Found"})
			return
		}
		err = searchqueryDb.All(ctx, &SearchProduct)
		if err != nil {
			log.Println(err)
			c.IndentedJSON(400, gin.H{"error": "Something went wrong"})
			return
		}
		defer searchqueryDb.Close(ctx)

		if er := searchqueryDb.Err(); er != nil {
			log.Println(er)
			c.IndentedJSON(400, gin.H{"error": "Something went wrong"})
			return
		}
		defer cancel()
		c.IndentedJSON(http.StatusOK, SearchProduct)

	}
}
